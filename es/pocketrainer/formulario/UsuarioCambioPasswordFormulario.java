package es.pocketrainer.formulario;

public class UsuarioCambioPasswordFormulario {

	private String passwordAnterior;
	private String password;
	private String passwordRepeticion;

	public String getPasswordAnterior() {
		return passwordAnterior;
	}

	public void setPasswordAnterior(String passwordAnterior) {
		this.passwordAnterior = passwordAnterior;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getPasswordRepeticion() {
		return passwordRepeticion;
	}

	public void setPasswordRepeticion(String passwordRepeticion) {
		this.passwordRepeticion = passwordRepeticion;
	}

}
